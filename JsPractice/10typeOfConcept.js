// typeOf operator in java script to find the data type.
let x = 25;
console.log(typeof x);

x = "check type";
console.log(typeof x);

x = {
    name: "rahul"
}
console.log(typeof x);

let flag = false;
console.log(typeof flag);

let type = typeof flag;
console.log(typeof type);

